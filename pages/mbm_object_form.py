def to_epoch_ms(d: datetime.date | None) -> str | None:
    if not d: return None
    # ÎÇ†Ïßú 00:00 KST Í∏∞Ï§Ä epoch ms
    dt = datetime.datetime(d.year, d.month, d.day, 0, 0, 0)
    return str(int(time.mktime(dt.timetuple()) * 1000))

